     */
    public function store(Request $request)
    {
        $response = Http::asForm()->get('https://conext.asia/api/global-images');
        $result = $response->json('result');
        foreach ($result['images'] as $value) {
            Explore::create([
                'name' => $value['content'],
                'image' => $value['image_url']
            ]);
        }
    }

